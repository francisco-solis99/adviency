import { useState } from "react"
import './Sound.css'

const audio = new Audio('./jingle.mp3')
audio.volume = 0.2

export function Sound() {
  const [isPlay, setIsPlay] = useState(false)

  const handleClickToggleSound = () => {
    if(!isPlay) {
      audio.play()
    }
    else {
      audio.pause()
    }
    setIsPlay(!isPlay)
  }

  return (
    <div>
      <button type="button" className="sound__toggle-btn" onClick={handleClickToggleSound}>
      {
        isPlay == true
        ? (
          <svg xmlns="http://www.w3.org/2000/svg" width="28" height="28" viewBox="0 0 24 24">
            <path fill="currentColor" d="M14.077 19.494v-1.05q2.077-.727 3.346-2.5q1.27-1.773 1.27-3.969t-1.27-3.97q-1.27-1.772-3.346-2.5v-1.05q2.485.797 4.05 2.86q1.565 2.062 1.565 4.66t-1.565 4.66q-1.565 2.063-4.05 2.86M4.307 14v-4h3.424l3.577-3.577v11.154L7.73 14zm9.77 1.308V8.642q.848.55 1.29 1.448q.44.898.44 1.91q0 1.006-.45 1.882q-.451.876-1.28 1.426"/>
          </svg>
        )
        : (
          <svg xmlns="http://www.w3.org/2000/svg" width="28" height="28" viewBox="0 0 24 24">
            <path fill="currentColor" d="M16.14 18.671q-.217.137-.453.267q-.237.131-.48.237q-.182.079-.396 0t-.288-.267q-.073-.183.028-.372q.1-.19.284-.269q.232-.094.43-.21t.387-.274l-4.075-3.675v2.217q0 .348-.324.476t-.568-.116L8 14H5.385q-.348 0-.578-.23q-.23-.23-.23-.578v-2.384q0-.348.23-.578q.23-.23.578-.23h2.084L2.946 5.477q-.14-.14-.15-.344q-.01-.204.15-.364t.354-.16q.194 0 .354.16L19.5 20.615q.14.14.15.345q.01.203-.15.363q-.16.16-.354.16q-.194 0-.354-.16zm2.23-2.006l-.7-.7q.617-.852.954-1.865q.338-1.013.338-2.125q0-2.037-1.11-3.74t-2.979-2.533q-.202-.098-.3-.278q-.098-.18-.011-.362q.092-.208.316-.268q.224-.06.45.039q2.099.979 3.366 2.89q1.268 1.912 1.268 4.252q0 1.306-.42 2.502t-1.173 2.188m-2.773-2.773l-1.25-1.25V9.408q0-.223.215-.322q.214-.1.387.031q.575.545.852 1.325q.277.781.277 1.558q0 .51-.12.988q-.12.477-.36.904m-4.02-4.019L9.862 8.138l.823-.823q.244-.244.568-.116q.324.128.324.476z"/>
          </svg>
        )
      }
      </button>
    </div>
  )
}
